// Multiply

function multiply(a, b){
    return a * b
}


// Even or Odd

function evenOrOdd(number) {
    if (number % 2 === 0) {
        return "Even"
    } else {
        return "Odd"
    }
}


// Even or Odd с использованием тернарного оператора ?

function evenOrOdd2(number) {
    return number % 2 === 0 ? "Even" : "Odd"
}
